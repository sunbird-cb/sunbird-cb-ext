package org.sunbird.workallocation.model.telemetryevent;

public class Pdata {
	private String id;
	private String pid;
	private String ver;

	public String getId() {
		return id;
	}

	public String getPid() {
		return pid;
	}

	public String getVer() {
		return ver;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public void setVer(String ver) {
		this.ver = ver;
	}
}
